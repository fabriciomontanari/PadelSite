CREATE TABLE CITAS_PADEL (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100) NOT NULL,
    fecha DATE NOT NULL,
    hora VARCHAR2(5) NOT NULL
);

CREATE INDEX IDX_FECHA on CITAS_PADEL(fecha);

ALTER TABLE CITAS_PADEL ADD CONSTRAINT UNIQUE_RESERVA UNIQUE (fecha, hora);

